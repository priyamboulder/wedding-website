export const STORE_CATEGORY_LABEL: Record<StoreCategory, string> = {
  attire: "Attire",
  jewelry: "Jewelry",
  decor: "Decor",
  stationery: "Stationery",
  catering_accessories: "Catering Accessories",
  gifting: "Gifting",
};

const PRODUCT_BY_ID = new Map(STORE_PRODUCTS.map((p) => [p.id, p]));

export function getStoreProduct(id: string | null): StoreProduct | null {
  if (!id) return null;
  return PRODUCT_BY_ID.get(id) ?? null;
}

export function priceWithVariant(product: StoreProduct, variantId: string | null): number {
  if (!variantId) return product.basePrice;
  const v = product.variants.find((x) => x.id === variantId);
  return product.basePrice + (v?.priceDelta ?? 0);
}

export function leadTimeWithVariant(
  product: StoreProduct,
  variantId: string | null,
): number {
  if (!variantId) return product.baseLeadTimeDays;
  const v = product.variants.find((x) => x.id === variantId);
  return product.baseLeadTimeDays + (v?.leadTimeDeltaDays ?? 0);
}

export function stockWithVariant(
  product: StoreProduct,
  variantId: string | null,
): StockStatus {
  if (!variantId) return product.stockStatus;
  const v = product.variants.find((x) => x.id === variantId);
  return v?.stockStatus ?? product.stockStatus;
}

