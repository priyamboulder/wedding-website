export function getStoreVendor(id: string | null): StoreVendor | null {
  if (!id) return null;
  return VENDOR_BY_ID.get(id) ?? null;
}

// ── Variant helpers ────────────────────────────────────────────────────────

function sizeVariants(stock: StockStatus = "in_stock"): StoreVariant[] {
  return [
    { id: "xs", label: "XS", attribute: "Size", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: stock, stockCount: null },
    { id: "s", label: "S", attribute: "Size", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: stock, stockCount: null },
    { id: "m", label: "M", attribute: "Size", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: stock, stockCount: null },
    { id: "l", label: "L", attribute: "Size", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: stock, stockCount: null },
    { id: "xl", label: "XL", attribute: "Size", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: stock, stockCount: null },
  ];
}

function colorVariants(colors: { id: string; label: string; stock?: StockStatus }[]): StoreVariant[] {
  return colors.map((c) => ({
    id: c.id,
    label: c.label,
    attribute: "Color",
    priceDelta: 0,
    leadTimeDeltaDays: 0,
    stockStatus: c.stock ?? "in_stock",
    stockCount: null,
  }));
}

function monogramOption(): StoreVariant[] {
  return [
    { id: "plain", label: "Plain", attribute: "Customization", priceDelta: 0, leadTimeDeltaDays: 0, stockStatus: "in_stock", stockCount: null },
    { id: "monogrammed", label: "Monogrammed", attribute: "Customization", priceDelta: 1800, leadTimeDeltaDays: 14, stockStatus: "made_to_order", stockCount: null },
  ];
}

